public class Login
{
	public String username;
	public String password;
	
	public String getUsername()
	{
		return username;
	}
	public String getPassword()
	{
		return password;
	}
	
	public void setUsername(String u)
	{
		username = u;
	}
	public void setPassword(String p)
	{
		password = p;
	}
}